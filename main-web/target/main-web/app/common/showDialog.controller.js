(function () {
    'use strict';

    angular
        .module('devplatformApp')
        .controller('ShowDialogController', ShowDialogController);

    ShowDialogController.$inject = ['$scope'];

    function ShowDialogController( $scope) {
        $scope.showDialog ={
            msg : $scope.dialog ? $scope.dialog.msg : "保存成功"
        }
    }

})();
